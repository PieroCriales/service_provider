     * Remove the specified resource from storage.
     *
     * @param  \App\Models\Like  $like
     * @return \Illuminate\Http\Response
     */
    public function destroy(Like $like)
    {
        Post::where('id', $like->get('post_id'))->update(['num_likes' => DB::raw('num_likes - 1')]);
